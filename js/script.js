function setColor(color) {
    document.body.style.background = color;
}

function setGradient() {
    document.body.style.background =
        "linear-gradient(135deg, #ff7e5f, #feb47b)";
}

function setImage() {
    document.body.style.background =
        "url(https://images.unsplash.com/photo-1478098711619-5ab0b478d6e6?w=600&auto=format&fit=crop&q=60&ixlib=rb-4.1.0&ixid=M3wxMjA3fDB8MHxzZWFyY2h8MTZ8fGNhdHxlbnwwfHwwfHx8MA%3D%3D') no-repeat center center/cover";
}

function resetBg() {
    document.body.style.background =
        "linear-gradient(135deg, #667eea, #764ba2)";
}